    public List<List<Integer>> threeSum(int[] nums) {
        List<List<Integer>> list = new ArrayList<>();
        Set<String> sets = new HashSet<>();
        for (int i = 0; i < nums.length - 2; i++) {
            int target = 0 - nums[i];
            int[] temp = new int[nums.length - i - 1];
            System.arraycopy(nums, i + 1, temp, 0, temp.length);
            List<List<Integer>> integers = twoSum(target, temp);
            if (integers.size() > 0) {
                for (List<Integer> l : integers) {
                    l.add(nums[i]);
                    String key = genStr(l);
                    if (!sets.contains(key)) {
                        sets.add(key);
                        list.add(l);
                    }
                }
            }
        }
        return list;
    }

    private String genStr(List<Integer> list) {
        Set<Integer> sets = new TreeSet<>();
        String keys = "";
        for (Integer k : list) {
            sets.add(k);
        }
        Iterator<Integer> iterator = sets.iterator();
        while (iterator.hasNext()) {
            keys += iterator.next().toString();
        }
        //System.out.println(keys);
        return keys;
    }

    private List<List<Integer>> twoSum(int target, int[] nums) {
        List<List<Integer>> resultlist = new ArrayList<>();
        Map<Integer, Integer> map = new HashMap<>();
        for (int i = 0; i < nums.length; i++) {
            map.put(nums[i], i);
        }
        for (int i = 0; i < nums.length; i++) {
            if (map.containsKey(target - nums[i]) && map.get(target - nums[i]) != i) {
                List<Integer> list = new ArrayList<>();
                list.add(nums[i]);
                list.add(target - nums[i]);
                resultlist.add(list);
            }
        }
        return resultlist;
    }

